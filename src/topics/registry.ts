/** Lookup map: slug → TopicMeta */
export const topicMap: Record<string, TopicMeta> = Object.fromEntries(
  topicList.map((t) => [t.slug, t])
);

/** Look up a single topic by slug. Returns undefined if not found. */
export function getTopicBySlug(slug: string): TopicMeta | undefined {
  return topicMap[slug];
}

/** Get all topic metadata as an array. */
export function getAllTopics(): TopicMeta[] {
  return topicList;
}

/** Get all topics belonging to a specific category. */
export function getTopicsByCategory(categorySlug: string): TopicMeta[] {
  return topicList.filter((t) => t.category === categorySlug);
}
